BinaryFileParser::BinaryFileParser( const std::string& filePath )
    : BinaryBufferParser()
{
    m_fileLoadSuccess = LoadBinaryFileToExistingByteVector( filePath, m_byteVector );
    m_buffer = m_byteVector.data();
    m_bufferSize = m_byteVector.size();
    m_bufferPosition = 0;
    m_hasReachedEndOfData = false;

}

///---------------------------------------------------------------------------------
///
///---------------------------------------------------------------------------------
BinaryFileParser::BinaryFileParser()
    : BinaryBufferParser()
{

}

