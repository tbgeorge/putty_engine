void WidgetPropertyLayer::CopyFromLayer( const WidgetPropertyLayer& propsToCopy )
{
    m_defaults.AddProperties( propsToCopy.m_defaults );

    for (int stateNum = 0; stateNum < WidgetState_NUM_STATES; stateNum++)
        m_defaultsByState[stateNum].AddProperties( propsToCopy.m_defaultsByState[stateNum] );
}