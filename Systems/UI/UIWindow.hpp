#pragma once

#ifndef __included_UIWindow__
#define __included_UIWindow__

////---------------------------------------------------------------------------------
///
// Includes
///
////---------------------------------------------------------------------------------
#include <vector>
#include "BaseWidget.hpp"

////---------------------------------------------------------------------------------
///
// class UIWindow
///
////---------------------------------------------------------------------------------
class UIWindow
{
public:
    ///---------------------------------------------------------------------------------
    /// Constructors/Destructors
    ///---------------------------------------------------------------------------------
    UIWindow() {};
    ~UIWindow() {};

    ///---------------------------------------------------------------------------------
    /// Accessors/Queries
    ///---------------------------------------------------------------------------------
    std::string GetName() { return m_windowName; }

    ///---------------------------------------------------------------------------------
    /// Mutators
    ///---------------------------------------------------------------------------------
    void SetName( const std::string& name ) { m_windowName = name; }
    void AddChildWidget( BaseWidget* widget ) { m_children.push_back( widget ); }

    ///---------------------------------------------------------------------------------
    /// Update
    ///---------------------------------------------------------------------------------
    void ProcessInput( InputSystem* inputSystem );
    void Update();

    ///---------------------------------------------------------------------------------
    /// Render
    ///---------------------------------------------------------------------------------
    void Render( OpenGLRenderer* renderer );

private:
    ///---------------------------------------------------------------------------------
    /// Private member variables
    ///---------------------------------------------------------------------------------
    std::vector< BaseWidget* > m_children;
    bool m_isActive;
    std::string m_windowName;

};

#endif
