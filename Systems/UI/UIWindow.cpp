void UIWindow::ProcessInput( InputSystem* inputSystem )
{
    for (auto iter = m_children.begin(); iter != m_children.end(); ++iter)
    {
        BaseWidget* child = *iter;
        child->ProcessInput( inputSystem );
    }
}

///---------------------------------------------------------------------------------
///
///---------------------------------------------------------------------------------
void UIWindow::Update()
{
    for (auto iter = m_children.begin(); iter != m_children.end(); ++iter)
    {
        BaseWidget* child = *iter;
        child->Update();
    }
}

////---------------------------------------------------------------------------------
///
// Render
///
////---------------------------------------------------------------------------------

///---------------------------------------------------------------------------------
///
///---------------------------------------------------------------------------------
void UIWindow::Render( OpenGLRenderer* renderer )
{
    if (!renderer)
        return;

    for (auto iter = m_children.begin(); iter != m_children.end(); ++iter)
    {
        BaseWidget* child = *iter;
        child->Render( renderer );
    }
}